package handler

import (
	"net/http"

	"github.com/zeromicro/go-zero/rest/httpx"
	"river/goZero/internal/logic"
	"river/goZero/internal/svc"
	"river/goZero/internal/types"
)

func GoZeroHandler(svcCtx *svc.ServiceContext) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		var req types.Request
		if err := httpx.Parse(r, &req); err != nil {
			httpx.ErrorCtx(r.Context(), w, err)
			return
		}

		l := logic.NewGoZeroLogic(r.Context(), svcCtx)
		resp, err := l.GoZero(&req)
		if err != nil {
			httpx.ErrorCtx(r.Context(), w, err)
		} else {
			httpx.OkJsonCtx(r.Context(), w, resp)
		}
	}
}
